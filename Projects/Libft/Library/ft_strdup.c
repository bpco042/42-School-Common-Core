#include "libft.h"

char    *ft_strdup(const char *src)
{
    size_t len;
    char *dup;

    len = ft_strlen(src) + 1;
    dup = malloc(len);

    if (!dup)
        return (NULL);

    ft_memcpy(dup, src, len);

    return (dup);
}